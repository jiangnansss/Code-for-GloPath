def vit_base(patch_size=16, **kwargs):
    model = VisionTransformer(
        patch_size=patch_size, embed_dim=768, depth=12, num_heads=12, mlp_ratio=4,
        qkv_bias=True, norm_layer=partial(nn.LayerNorm, eps=1e-6), **kwargs)
    model.out_channels = 16
    return model

def loadGloPath(numClasses=1000, pretrain='glopath_weight_0.pth'):

    model = vit_base(num_classes=numClasses)
    try:
        weight = torch.load(pretrain, map_location='cpu')
        new_weight = {}
        for k, v in weight.items():
            if k == 'head.weight' or k == 'head.bias':
                continue
            newK = k.replace('backbone.', '') if 'backbone.' in k else k
            newK = newK.replace('module.', '') if 'module.' in newK else newK
            new_weight[newK] = v
        model.load_state_dict(new_weight, strict=False)
    except:
        pass # if model weight is unavailable

    return model

